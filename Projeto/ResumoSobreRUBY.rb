# Ruby GEMS:

#É um sistema que ajuda os programadores a gerenciar e compartilhar bibliotecas de código (também conhecidas como "gemas" ou "gems" ) em Ruby. Uma gema é um pacote de
#software que pode conter código, documentação, ou qualquer coisa que possa ser útil para um projeto. Com o RubyGems, os desenvolvedores podem facilmente instalar, atualizar e
#compartilhar essas bibliotecas para melhorar a eficiência e a reutilização de código em projetos Ruby. É como uma loja de aplicativos, onde você pode encontrar e baixar funcionalidades
#prontas para serem usadas em seus programas.
